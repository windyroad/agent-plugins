#!/usr/bin/env node
// Generate Codex custom-agent TOML from Claude agent markdown without
// changing the Claude agent source.

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, "..");
const SOURCE = join(REPO_ROOT, "packages", "architect", "agents", "agent.md");
const TARGET = join(REPO_ROOT, ".codex", "agents", "wr-architect.toml");
const mode = process.argv.includes("--check") ? "check" : "sync";

function splitFrontmatter(markdown) {
  if (!markdown.startsWith("---\n")) {
    return { frontmatter: "", body: markdown };
  }
  const end = markdown.indexOf("\n---\n", 4);
  if (end === -1) {
    return { frontmatter: "", body: markdown };
  }
  return {
    frontmatter: markdown.slice(4, end),
    body: markdown.slice(end + "\n---\n".length),
  };
}

function frontmatterValue(frontmatter, key) {
  const lines = frontmatter.split(/\r?\n/);
  const start = lines.findIndex((line) => line.startsWith(`${key}:`));
  if (start === -1) return "";
  const values = [lines[start].slice(key.length + 1).trim()];
  for (let i = start + 1; i < lines.length; i += 1) {
    if (/^[A-Za-z0-9_-]+:/.test(lines[i])) break;
    if (/^\s+/.test(lines[i])) values.push(lines[i].trim());
  }
  return values.filter(Boolean).join(" ");
}

function tomlMultiline(value) {
  return value.replace(/\\/g, "\\\\").replace(/"""/g, '\\"\\"\\"').trimEnd();
}

function buildAgent() {
  const source = readFileSync(SOURCE, "utf8");
  const { frontmatter, body } = splitFrontmatter(source);
  const description =
    frontmatterValue(frontmatter, "description") ||
    "Architecture reviewer for structural and technology decisions.";

  return [
    "# Generated by scripts/sync-codex-agents.mjs from packages/architect/agents/agent.md.",
    "# Do not edit by hand; update the Claude agent markdown and re-run npm run sync:codex-agents.",
    'name = "wr-architect"',
    `description = ${JSON.stringify(description)}`,
    'sandbox_mode = "read-only"',
    'developer_instructions = """',
    tomlMultiline(body),
    '"""',
    "",
  ].join("\n");
}

const expected = buildAgent();
const current = existsSync(TARGET) ? readFileSync(TARGET, "utf8") : "";

if (mode === "check") {
  if (current !== expected) {
    console.error("DIVERGED: .codex/agents/wr-architect.toml is out of sync");
    console.error("Run: npm run sync:codex-agents");
    process.exit(1);
  }
  console.log("OK: .codex/agents/wr-architect.toml matches packages/architect/agents/agent.md");
  process.exit(0);
}

mkdirSync(dirname(TARGET), { recursive: true });
if (current === expected) {
  console.log("OK: .codex/agents/wr-architect.toml already in sync");
} else {
  writeFileSync(TARGET, expected, "utf8");
  console.log("synced: .codex/agents/wr-architect.toml");
}
